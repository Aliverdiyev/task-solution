package beginner;
/*
Define a data type for each of these fields
   1. 0.767676779
   2. 128
   3. 58675864945
   4. A
   5. False
   6. 0.7676
   7. true
*/
public class DefineTypeDemo {
    public static void main(String[] args) {
        double x = 0.767676779;
        int z = 128;
        long l = 58675864945L;
        char y = 'A';
        boolean b = false;
        float f = 0.7676f;
        boolean t = true;
    }
}